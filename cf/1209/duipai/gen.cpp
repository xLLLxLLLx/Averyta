#include <bits/stdc++.h>
using namespace std;

int main() {
  srand(time(NULL));
  int T = rand() % 10 + 1;
  printf("%d\n", T);
  for(int o = 1; o <= T; ++o) {
    int x = rand() % 4 + 1, y = rand() % 10 + 1;
    printf("%d %d\n", x, y);
    for(int i = 1; i <= x; ++i) {
      for(int j = 1; j <= y; ++j) {
        int w = rand() % 100 + 1;
        printf("%d ", w);
      }
      puts("");
    }
  }
  return 0;
}
